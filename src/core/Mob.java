package core;

public class Mob {
    public String name;
    public String description;
    public String health;
    public String mana;
    public String endurance;
    public String spirit;
    public Mob(String name, String description, String health, String mana, String endurance, String spirit) {
        this.name = name;
        this.description = description;
        this.health = health;
        this.mana = mana;
        this.endurance = endurance;
        this.spirit = spirit;
    }
}
